"use babel";
export var WidthChangeMode;
(function (WidthChangeMode) {
    WidthChangeMode[WidthChangeMode["Reset"] = 0] = "Reset";
    WidthChangeMode[WidthChangeMode["Increase"] = 1] = "Increase";
    WidthChangeMode[WidthChangeMode["Decrease"] = 2] = "Decrease";
})(WidthChangeMode || (WidthChangeMode = {}));
